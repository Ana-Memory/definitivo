    private void insertarProducto(String nombre, String descripcion, String tipo, double precio, int unidades) {
    String sql = "INSERT INTO producto (nombre, descripcion, tipo, precio, unidades) VALUES (?, ?, ?, ?, ?)";

    try (Connection con = Conexion.getConexion();
         PreparedStatement ps = con.prepareStatement(sql)) {

        ps.setString(1, nombre);
        ps.setString(2, descripcion);
        ps.setString(3, tipo);
        ps.setDouble(4, precio);
        ps.setInt(5, unidades);

        int filas = ps.executeUpdate();
        if (filas > 0) {
            JOptionPane.showMessageDialog(this, "Producto insertado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo insertar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al insertar producto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
