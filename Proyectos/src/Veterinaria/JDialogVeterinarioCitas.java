    public JDialogVeterinarioCitas(java.awt.Dialog parent, boolean modal) {
        super(parent, modal);
        initComponents();
        ajustarImagenLogo();
        
        modelo = new DefaultListModel<>();
        jListCita.setModel(modelo);

        String sql = "SELECT r.id AS id_reserva, r.id_dueno, r.fecha, r.hora, c.id AS id_consulta, c.tipo, m.nombre AS nombre_mascota, m.especie " +
                     "FROM reserva r " +
                     "JOIN consulta c ON r.id_consulta = c.id " +
                     "JOIN mascota m ON c.id_mascota = m.id";

        try (Connection con = Conexion.getConexion()) {
             PreparedStatement ps = con.prepareStatement(sql);
             ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String idDueno = rs.getString("id_dueno");
                if (idDueno == null || idDueno.isEmpty()) {
                    idDueno = "La mascota no tiene dueño";
                }

                id_reserva = rs.getInt("id_reserva");
                id_consulta = rs.getInt("id_consulta");
                listaIdReserva.add(id_reserva);
                listaIdConsulta.add(id_consulta);
                Date fecha = rs.getDate("fecha");
                java.sql.Time hora = rs.getTime("hora");
                String nombreMascota = rs.getString("nombre_mascota");
                String especie = rs.getString("especie");
                String tipoConsulta = rs.getString("tipo");

                String item = String.format("Dueño: %s | Fecha: %s | Hora: %s | Mascota: %s (%s) | Motivo de la visita: %s",
                                            idDueno, fecha.toString(), hora.toString(), nombreMascota, especie, tipoConsulta);

                modelo.addElement(item);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this,
                "Error al cargar las citas: " + ex.getMessage(),
                "Error BD",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

