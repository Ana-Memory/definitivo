    private void jButtonAtenderCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtenderCitaActionPerformed
        int index = jListCita.getSelectedIndex();
        if (index == -1) {
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "¿Va a atender esta cita?", 
            "Confirmar", 
            javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

        int id_reserva_seleccionada = listaIdReserva.get(index);
        int id_consulta_seleccionada = listaIdConsulta.get(index);

        String sqlDeleteReserva = "DELETE FROM reserva WHERE id = ?";
        String sqlDeleteConsulta = "DELETE FROM consulta WHERE id = ?";

        try (Connection con = Conexion.getConexion()) {
            con.setAutoCommit(false);

            try (PreparedStatement psRes = con.prepareStatement(sqlDeleteReserva);
                 PreparedStatement psCon = con.prepareStatement(sqlDeleteConsulta)) {

                psRes.setInt(1, id_reserva_seleccionada);
                psRes.executeUpdate();

                psCon.setInt(1, id_consulta_seleccionada);
                psCon.executeUpdate();

                con.commit();
            } catch (SQLException ex) {
                con.rollback();
                throw ex;
            }

            modelo.remove(index);
            listaIdReserva.remove(index);
            listaIdConsulta.remove(index);

            javax.swing.JOptionPane.showMessageDialog(this, 
                "El paciente fue atentido.");
        } catch (SQLException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error al eliminar la cita: " + ex.getMessage(),
                "Error BD", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAtenderCitaActionPerformed
